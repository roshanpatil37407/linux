 Linux commands you mentioned, along with their attributes:

1. `pwd`: Print the current working directory.
   - Example: `pwd`

2. `cd`: Change the current directory.
   - Example: `cd /home/username`

3. `ls`: List files and directories.
   - Example: `ls -l` (long format), `ls -a` (including hidden files)

4. `more`: Display text files one screen at a time.
   - Example: `more filename.txt`

5. `less`: Display text files with backward navigation.
   - Example: `less filename.txt`

6. `echo`: Display a message on the terminal.
   - Example: `echo "Hello, World!"`

7. `clear`: Clear the terminal screen.
   - Example: `clear`

8. `kill`: Terminate processes by sending signals.
   - Example: `kill -9 <process_id>`

9. `ps`: Display information about active processes.
   - Example: `ps aux`

10. `man`: Display the manual page of a command.
    - Example: `man ls`

11. `cal`: Display a calendar for the current month.
    - Example: `cal`

12. `date`: Display the current date and time.
    - Example: `date`

13. `who`: Display information about logged-in users.
    - Example: `who`

14. `whoami`: Display the current username.
    - Example: `whoami`

15. `wc`: Count words, lines, and characters in a file.
    - Example: `wc -l filename.txt`

16. `mkdir`: Create a new directory.
    - Example: `mkdir new_directory`

17. `rmdir`: Remove an empty directory.
    - Example: `rmdir empty_directory`

18. `rm`: Remove files or directories.
    - Example: `rm file.txt`, `rm -r directory`

19. `sort`: Sort lines of text files.
    - Example: `sort filename.txt